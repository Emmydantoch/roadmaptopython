# REPL = Read-Eval-Print Loop
# REPL is a simple interactive computer programming environment that takes single user inputs, executes them, and returns the result to the user.
# It is a simple way to test code snippets and get immediate feedback.
# It is commonly used in programming languages like Python, Ruby, and JavaScript.
# In Python, you can use the REPL by opening a terminal and typing `python` or `python3` to enter the interactive mode.
# You can then type Python code and see the results immediately.
# For example, you can type `print("Hello, World!")` and see the output right away.
# The REPL is a great way to experiment with code and learn how to use a programming language.
# It allows you to test small pieces of code without having to write a complete program.
# You can also use the REPL to debug code by testing small pieces of code and seeing how they behave.

